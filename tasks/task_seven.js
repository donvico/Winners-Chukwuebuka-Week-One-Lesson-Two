//using the switch statment
const dayOfWeek = "Monday";

switch (dayOfWeek) {
  case "Monday":
    console.log("Monday: Start of the week");
    break;
  case "Tuesday":
    console.log("Tuesday: Work mode");
    break;
  case "Wednesday":
    console.log("Wednesday: Midweek is here");
    break;
  case "Thursday":
    console.log("Thursday: i can smell the weekend");
    break;
  case "Friday":
    console.log("Friday: come lets party!");
    break;
  case "Saturday":
    console.log("Saturday: Weekend vibes");
    break;
  case "Sunday":
    console.log("Sunday: if your a christain lets go to church");
    break;
  default:
    console.log("Ha! this one no follow o");
}
